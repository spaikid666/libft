#include <stdio.h>

int ft_isascii(int c)
{
    if(c >= 0 && c<= 127)
        return 1;
    else
        return 0;
}

int main()
{
    char    c;
    int i;

    c = 'e';
    i = ft_isascii(c);
    printf("%d\n", i);
}
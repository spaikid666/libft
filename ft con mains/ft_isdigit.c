#include <stdio.h>

int ft_isdigit(int c)
{
    if(c >= '0' && c<= '9')
        return 1;
    else
        return 0;
}

int main()
{
    int n;
    int i;

    n = '4';
    i = ft_isdigit(n);
    printf("%d\n", i);
}